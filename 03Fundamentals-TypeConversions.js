// Conversion de datos en Javascript.

// ToString
let value = true;

alert(typeof value);

// Convertimos en String.
value = String(value);

alert(typeof value);

// ToNumber
let str = "123";

alert(typeof str);

str = Number(str);

alert(typeof str);

// ToBoolean
alert(Boolean(1));
alert(Boolean(0));

alert(Boolean("hello"));
alert(Boolean(""));
