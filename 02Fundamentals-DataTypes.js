// Tipos de datos en Javascript

// Number
let n1, n2;

n1 = 123;
n2 = 12.3;

// Special numeric values.
// Infinity.
alert(1 / 0);
alert(Infinity);
// NaN.
alert("Not a number" / 2);


// String
let strvalue, strvalue2;

strvalue = 'Esto es un String';
strvalue2 = "Hello";
strvalue3 = "World";

let phrase = `Podemos concatenar Strings Ej: ${strvalue}`;

alert(phrase);
console.log(strvalue2 + " " + strvalue3);

// Booleans
let isChecked = true;
let isFalse = 1 > 2;

console.log(isChecked);
console.log(isFalse);

// Null and Undefined
let x;
let nullValue = null;

alert(x);
alert(nullValue);

// Typeof -> muestra el tipo de la variable.
alert(typeof 0);
